namespace Spakll.CodeMash
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class LoadingPage : Page
    {
        public LoadingPage()
        {
            this.InitializeComponent();
            CodeMashData.Instance.Load(CodeMashData.Instance.ServiceUri);
            TickTock();
        }

        /// <summary>
        /// Invoked when this page is about to be displayed in a Frame.
        /// </summary>
        /// <param name="e">Event data that describes how this page was reached.  The Parameter
        /// property is typically used to configure the page.</param>
        protected override void OnNavigatedTo(NavigationEventArgs e)
        {
        }

        private async void TickTock()
        {

            while (!CodeMashData.Instance.Loaded.HasValue)
            {
                await Task.Delay(TimeSpan.FromSeconds(1));
            }

            this.Frame.Navigate(typeof(GroupedItemsPage), "AllGroups");

        }
    }
}
